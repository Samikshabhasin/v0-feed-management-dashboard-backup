"use client"

import { useState, useMemo } from "react"
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card"
import { Table, TableBody, TableCell, TableHead, TableHeader, TableRow } from "@/components/ui/table"
import { Badge } from "@/components/ui/badge"
import { Progress } from "@/components/ui/progress"
import { Select, SelectContent, SelectItem, SelectTrigger, SelectValue } from "@/components/ui/select"
import { Tooltip, TooltipContent, TooltipProvider, TooltipTrigger } from "@/components/ui/tooltip"
import {
  Eye,
  AlertTriangle,
  TrendingDown,
  ExternalLink,
  Download,
  ArrowUpDown,
  ArrowUp,
  ArrowDown,
  Info,
} from "lucide-react"
import { BarChart3 } from "lucide-react"
import type { Product } from "@/types/product"

interface SegmentResultsProps {
  products: Product[]
}

const segmentResults = [
  {
    name: "High-Value Electronics",
    products: 45,
    revenue: 125000,
    avgVisibility: 82.5,
    status: "active",
  },
  {
    name: "Nike Footwear",
    products: 23,
    revenue: 89000,
    avgVisibility: 78.2,
    status: "active",
  },
  {
    name: "Low Stock Items",
    products: 67,
    revenue: 34000,
    avgVisibility: 45.8,
    status: "needs_attention",
  },
]

const columnTooltips = {
  product: "The product name, ID, and category information",
  visibilityScore: "AI-calculated score (0-100) measuring how visible this product is across all connected channels",
  revenue: "Total revenue generated by this product in the selected time period",
  impressions: "Number of times this product appeared across all connected channels",
  ctr: "Click-through rate - percentage of impressions that resulted in clicks",
  issues: "Problems detected that may be affecting product performance",
  feedSources: "Marketing channels where this product is currently active",
}

export function SegmentResults({ products }: SegmentResultsProps) {
  const [sortColumn, setSortColumn] = useState<string | null>(null)
  const [sortDirection, setSortDirection] = useState<"asc" | "desc">("desc")

  const [categoryFilter, setCategoryFilter] = useState<string>("all")
  const [statusFilter, setStatusFilter] = useState<string>("all")
  const [feedSourceFilter, setFeedSourceFilter] = useState<string>("all")

  const uniqueCategories = useMemo(() => {
    const categories = new Set(products.map((p) => p.category))
    return Array.from(categories).sort()
  }, [products])

  const uniqueStatuses = useMemo(() => {
    const statuses = new Set(products.map((p) => p.approvalStatus))
    return Array.from(statuses).sort()
  }, [products])

  const uniqueFeedSources = useMemo(() => {
    const sources = new Set<string>()
    products.forEach((p) => {
      if (p.feedSource) {
        sources.add(p.feedSource)
      }
    })
    return Array.from(sources).sort()
  }, [products])

  const filteredAndSortedProducts = useMemo(() => {
    let filtered = [...products]

    if (categoryFilter !== "all") {
      filtered = filtered.filter((p) => p.category === categoryFilter)
    }
    if (statusFilter !== "all") {
      filtered = filtered.filter((p) => p.approvalStatus === statusFilter)
    }
    if (feedSourceFilter !== "all") {
      filtered = filtered.filter((p) => p.feedSource === feedSourceFilter)
    }

    if (sortColumn) {
      filtered.sort((a, b) => {
        let aValue: any = a[sortColumn as keyof Product]
        let bValue: any = b[sortColumn as keyof Product]

        if (aValue === undefined || aValue === null) aValue = 0
        if (bValue === undefined || bValue === null) bValue = 0

        if (typeof aValue === "number" && typeof bValue === "number") {
          return sortDirection === "asc" ? aValue - bValue : bValue - aValue
        }

        return sortDirection === "asc"
          ? String(aValue).localeCompare(String(bValue))
          : String(bValue).localeCompare(String(aValue))
      })
    }

    return filtered
  }, [products, categoryFilter, statusFilter, feedSourceFilter, sortColumn, sortDirection])

  const handleSort = (column: string) => {
    if (sortColumn === column) {
      setSortDirection(sortDirection === "asc" ? "desc" : "asc")
    } else {
      setSortColumn(column)
      setSortDirection("desc")
    }
  }

  const renderSortIcon = (column: string) => {
    if (sortColumn !== column) {
      return <ArrowUpDown className="h-3 w-3 ml-1 opacity-50" />
    }
    return sortDirection === "asc" ? <ArrowUp className="h-3 w-3 ml-1" /> : <ArrowDown className="h-3 w-3 ml-1" />
  }

  const totalResults = filteredAndSortedProducts.length
  const avgVisibilityScore =
    totalResults > 0
      ? Math.round(filteredAndSortedProducts.reduce((sum, product) => sum + product.visibilityScore, 0) / totalResults)
      : 0
  const criticalProducts = filteredAndSortedProducts.filter((p) => p.visibilityScore < 30).length

  const getStatusBadge = (status: string) => {
    switch (status) {
      case "active":
        return <Badge className="bg-green-100 text-green-800">Active</Badge>
      case "needs_attention":
        return <Badge className="bg-yellow-100 text-yellow-800">Needs Attention</Badge>
      default:
        return <Badge variant="secondary">{status}</Badge>
    }
  }

  if (products.length === 0) {
    return (
      <Card>
        <CardContent className="text-center py-8">
          <Eye className="h-12 w-12 mx-auto mb-4 opacity-50" />
          <p className="text-muted-foreground">Build a segment to see results</p>
        </CardContent>
      </Card>
    )
  }

  return (
    <div className="space-y-4">
      {/* Results Summary */}
      <div className="grid gap-4 md:grid-cols-4">
        <Card>
          <CardHeader className="flex flex-row items-center justify-between space-y-0 pb-2">
            <CardTitle className="text-sm font-medium">Total Products</CardTitle>
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">{totalResults.toLocaleString()}</div>
            <p className="text-xs text-muted-foreground">matching your criteria</p>
          </CardContent>
        </Card>

        <Card>
          <CardHeader className="flex flex-row items-center justify-between space-y-0 pb-2">
            <CardTitle className="text-sm font-medium">Avg Visibility Score</CardTitle>
            <Eye className="h-4 w-4 text-muted-foreground" />
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">{avgVisibilityScore}/100</div>
            <Progress value={avgVisibilityScore} className="mt-2" />
          </CardContent>
        </Card>

        <Card>
          <CardHeader className="flex flex-row items-center justify-between space-y-0 pb-2">
            <CardTitle className="text-sm font-medium">Critical Issues</CardTitle>
            <AlertTriangle className="h-4 w-4 text-red-500" />
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold text-red-600">{criticalProducts}</div>
            <p className="text-xs text-muted-foreground">products need immediate attention</p>
          </CardContent>
        </Card>

        <Card>
          <CardHeader className="flex flex-row items-center justify-between space-y-0 pb-2">
            <CardTitle className="text-sm font-medium">Potential Revenue</CardTitle>
            <TrendingDown className="h-4 w-4 text-muted-foreground" />
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">$45.2K</div>
            <p className="text-xs text-muted-foreground">at risk from low visibility</p>
          </CardContent>
        </Card>
      </div>

      {/* Results Table */}
      <Card>
        <CardHeader>
          <div className="flex items-center justify-between">
            <div>
              <CardTitle>Product Grid</CardTitle>
              <CardDescription>Products matching your filter criteria</CardDescription>
            </div>
            <div className="flex items-center space-x-2">
              <button className="inline-flex items-center justify-center rounded-md text-sm font-medium ring-offset-background transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 border border-input bg-background hover:bg-accent hover:text-accent-foreground h-9 px-3">
                <Download className="h-4 w-4 mr-2" />
                Export
              </button>
            </div>
          </div>

          <div className="flex items-center gap-3 pt-4">
            <div className="flex items-center gap-2">
              <label className="text-sm font-medium">Category:</label>
              <Select value={categoryFilter} onValueChange={setCategoryFilter}>
                <SelectTrigger className="w-[180px]">
                  <SelectValue placeholder="All Categories" />
                </SelectTrigger>
                <SelectContent>
                  <SelectItem value="all">All Categories</SelectItem>
                  {uniqueCategories.map((category) => (
                    <SelectItem key={category} value={category}>
                      {category}
                    </SelectItem>
                  ))}
                </SelectContent>
              </Select>
            </div>

            <div className="flex items-center gap-2">
              <label className="text-sm font-medium">Status:</label>
              <Select value={statusFilter} onValueChange={setStatusFilter}>
                <SelectTrigger className="w-[180px]">
                  <SelectValue placeholder="All Statuses" />
                </SelectTrigger>
                <SelectContent>
                  <SelectItem value="all">All Statuses</SelectItem>
                  {uniqueStatuses.map((status) => (
                    <SelectItem key={status} value={status} className="capitalize">
                      {status}
                    </SelectItem>
                  ))}
                </SelectContent>
              </Select>
            </div>

            <div className="flex items-center gap-2">
              <label className="text-sm font-medium">Channel:</label>
              <Select value={feedSourceFilter} onValueChange={setFeedSourceFilter}>
                <SelectTrigger className="w-[180px]">
                  <SelectValue placeholder="All Channels" />
                </SelectTrigger>
                <SelectContent>
                  <SelectItem value="all">All Channels</SelectItem>
                  {uniqueFeedSources.map((source) => (
                    <SelectItem key={source} value={source}>
                      {source}
                    </SelectItem>
                  ))}
                </SelectContent>
              </Select>
            </div>
          </div>
        </CardHeader>
        <CardContent>
          <TooltipProvider>
            <Table>
              <TableHeader>
                <TableRow>
                  <TableHead>
                    <div className="flex items-center">
                      <span>Product</span>
                      <Tooltip>
                        <TooltipTrigger asChild>
                          <Info className="h-3 w-3 ml-1 text-muted-foreground cursor-help" />
                        </TooltipTrigger>
                        <TooltipContent>
                          <p className="max-w-xs">{columnTooltips.product}</p>
                        </TooltipContent>
                      </Tooltip>
                    </div>
                  </TableHead>
                  <TableHead>
                    <button
                      onClick={() => handleSort("visibilityScore")}
                      className="flex items-center hover:text-foreground transition-colors"
                    >
                      <span>Visibility Score</span>
                      <Tooltip>
                        <TooltipTrigger asChild>
                          <Info className="h-3 w-3 ml-1 text-muted-foreground cursor-help" />
                        </TooltipTrigger>
                        <TooltipContent>
                          <p className="max-w-xs">{columnTooltips.visibilityScore}</p>
                        </TooltipContent>
                      </Tooltip>
                      {renderSortIcon("visibilityScore")}
                    </button>
                  </TableHead>
                  <TableHead>
                    <div className="flex items-center">
                      <span>Performance</span>
                      <Tooltip>
                        <TooltipTrigger asChild>
                          <Info className="h-3 w-3 ml-1 text-muted-foreground cursor-help" />
                        </TooltipTrigger>
                        <TooltipContent>
                          <p className="max-w-xs">Key performance metrics including revenue, impressions, and CTR</p>
                        </TooltipContent>
                      </Tooltip>
                    </div>
                    <div className="flex gap-2 mt-1 text-xs text-muted-foreground">
                      <button
                        onClick={() => handleSort("revenue")}
                        className="flex items-center hover:text-foreground transition-colors"
                      >
                        Revenue{renderSortIcon("revenue")}
                      </button>
                      <button
                        onClick={() => handleSort("impressions")}
                        className="flex items-center hover:text-foreground transition-colors"
                      >
                        Impressions{renderSortIcon("impressions")}
                      </button>
                      <button
                        onClick={() => handleSort("ctr")}
                        className="flex items-center hover:text-foreground transition-colors"
                      >
                        CTR{renderSortIcon("ctr")}
                      </button>
                    </div>
                  </TableHead>
                  <TableHead>
                    <div className="flex items-center">
                      <span>Issues</span>
                      <Tooltip>
                        <TooltipTrigger asChild>
                          <Info className="h-3 w-3 ml-1 text-muted-foreground cursor-help" />
                        </TooltipTrigger>
                        <TooltipContent>
                          <p className="max-w-xs">{columnTooltips.issues}</p>
                        </TooltipContent>
                      </Tooltip>
                    </div>
                  </TableHead>
                  <TableHead>
                    <div className="flex items-center">
                      <span>Feed Sources</span>
                      <Tooltip>
                        <TooltipTrigger asChild>
                          <Info className="h-3 w-3 ml-1 text-muted-foreground cursor-help" />
                        </TooltipTrigger>
                        <TooltipContent>
                          <p className="max-w-xs">{columnTooltips.feedSources}</p>
                        </TooltipContent>
                      </Tooltip>
                    </div>
                  </TableHead>
                  <TableHead>Actions</TableHead>
                </TableRow>
              </TableHeader>
              <TableBody>
                {filteredAndSortedProducts.map((product) => (
                  <TableRow key={product.id}>
                    <TableCell>
                      <div>
                        <div className="font-medium">{product.name}</div>
                        <div className="text-sm text-muted-foreground flex items-center space-x-2">
                          <span>{product.id}</span>
                          <Badge variant="outline">{product.category}</Badge>
                        </div>
                      </div>
                    </TableCell>
                    <TableCell>
                      <div className="flex items-center space-x-2">
                        <div
                          className={`w-3 h-3 rounded-full ${product.visibilityScore < 30 ? "bg-red-500" : product.visibilityScore < 50 ? "bg-yellow-500" : "bg-green-500"}`}
                        />
                        <span className="font-medium">{product.visibilityScore}/100</span>
                      </div>
                      <Progress value={product.visibilityScore} className="w-16 mt-1" />
                    </TableCell>
                    <TableCell>
                      <div className="text-sm space-y-1">
                        <div>Revenue: ${product.revenue?.toLocaleString() || 0}</div>
                        <div>Impressions: {product.impressions?.toLocaleString() || 0}</div>
                        <div>CTR: {product.ctr?.toFixed(1) || 0}%</div>
                      </div>
                    </TableCell>
                    <TableCell>
                      <div className="space-y-1">
                        {product.issues && product.issues.length > 0 ? (
                          <>
                            {product.issues.slice(0, 2).map((issue, index) => (
                              <Badge key={index} variant="destructive" className="text-xs mr-1">
                                {issue}
                              </Badge>
                            ))}
                            {product.issues.length > 2 && (
                              <Badge variant="outline" className="text-xs">
                                +{product.issues.length - 2} more
                              </Badge>
                            )}
                          </>
                        ) : (
                          <span className="text-xs text-muted-foreground">No issues</span>
                        )}
                      </div>
                    </TableCell>
                    <TableCell>
                      <div className="flex flex-wrap gap-1">
                        {product.feedSource && (
                          <Badge variant="secondary" className="text-xs">
                            {product.feedSource}
                          </Badge>
                        )}
                      </div>
                    </TableCell>
                    <TableCell>
                      <div className="flex items-center space-x-1">
                        <button className="inline-flex items-center justify-center rounded-md text-sm font-medium ring-offset-background transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 border border-input bg-background hover:bg-accent hover:text-accent-foreground h-8 w-8 p-0">
                          <ExternalLink className="h-3 w-3" />
                        </button>
                        <button className="inline-flex items-center justify-center rounded-md text-sm font-medium ring-offset-background transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 bg-primary text-primary-foreground hover:bg-primary/90 h-8 px-3 text-xs">
                          Fix Issues
                        </button>
                      </div>
                    </TableCell>
                  </TableRow>
                ))}
              </TableBody>
            </Table>
          </TooltipProvider>
        </CardContent>
      </Card>

      {/* Segment Performance Table */}
      <Card>
        <CardHeader>
          <CardTitle>Segment Performance</CardTitle>
          <CardDescription>Performance metrics for your product segments</CardDescription>
        </CardHeader>
        <CardContent>
          <Table>
            <TableHeader>
              <TableRow>
                <TableHead>Segment Name</TableHead>
                <TableHead>Products</TableHead>
                <TableHead>Revenue</TableHead>
                <TableHead>Avg Visibility</TableHead>
                <TableHead>Status</TableHead>
              </TableRow>
            </TableHeader>
            <TableBody>
              {segmentResults.map((segment) => (
                <TableRow key={segment.name}>
                  <TableCell className="font-medium">{segment.name}</TableCell>
                  <TableCell>{segment.products}</TableCell>
                  <TableCell>${segment.revenue.toLocaleString()}</TableCell>
                  <TableCell>
                    <div className="flex items-center space-x-2">
                      <Progress value={segment.avgVisibility} className="w-16" />
                      <span className="text-sm">{segment.avgVisibility}%</span>
                    </div>
                  </TableCell>
                  <TableCell>{getStatusBadge(segment.status)}</TableCell>
                </TableRow>
              ))}
            </TableBody>
          </Table>
        </CardContent>
      </Card>

      {/* Segment Results Visualization */}
      <Card>
        <CardHeader>
          <CardTitle>Segment Results</CardTitle>
          <CardDescription>Performance by segment</CardDescription>
        </CardHeader>
        <CardContent>
          <div className="h-[200px] flex items-center justify-center text-muted-foreground">
            <div className="text-center">
              <BarChart3 className="h-8 w-8 mx-auto mb-2" />
              <p>Segment results visualization</p>
            </div>
          </div>
        </CardContent>
      </Card>
    </div>
  )
}
